int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	n;

	i = 0;
	while (str[i])
	{
		n = str[i];
		if (!('a' <= n && n <= 'z'))
		{
			return (0);
		}
		i++;
	}
	return (1);
}
